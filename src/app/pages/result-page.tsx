import { useEffect, useState, useRef } from "react";
import { useNavigate, useParams } from "react-router";
import { supabase } from "../../lib/supabase";
import { NavLogo } from "../components/NavLogo";
import type { ScanResult } from "../../lib/supabase";

export function ResultPage() {
  const { scanId } = useParams();
  const navigate   = useNavigate();
  const [scan,   setScan]   = useState<ScanResult | null>(null);
  const [notes,  setNotes]  = useState("");
  const [saved,  setSaved]  = useState(false);
  const [saving, setSaving] = useState(false);
  const barsRef = useRef(false);

  useEffect(() => {
    // Load result from session storage first (freshly processed)
    const raw = sessionStorage.getItem("blnScanResult");
    if (raw) {
      try {
        const s = JSON.parse(raw) as ScanResult;
        if (s.id === scanId || raw) { setScan(s); setNotes(s.notes || ""); return; }
      } catch (_) {}
    }
    // Fallback: load from local cache
    const cached = JSON.parse(localStorage.getItem("blnScans") || "[]") as ScanResult[];
    const found  = cached.find(s => s.id === scanId);
    if (found) { setScan(found); setNotes(found.notes || ""); return; }
    // Fallback: load from Supabase
    supabase.from("scans").select("*").eq("id", scanId).single()
      .then(({ data }) => {
        if (data) {
          const s: ScanResult = {
            id: data.id, fileName: data.file_name, image: data.image_url,
            result: { classification: data.classification, confidence: data.confidence,
              probabilities: { normal: data.prob_normal, benign: data.prob_benign, malignant: data.prob_malignant },
              riskLevel: data.risk_level },
            date: data.created_at, notes: data.notes || "",
          };
          setScan(s); setNotes(s.notes);
        }
      });
  }, [scanId]);

  // Animate probability bars after scan loads
  useEffect(() => {
    if (!scan || barsRef.current) return;
    barsRef.current = true;
    setTimeout(() => {
      document.querySelectorAll<HTMLElement>(".bln-pbar-fill").forEach(el => {
        el.style.width = el.dataset.w + "%";
      });
    }, 400);
  }, [scan]);

  const saveNotes = async () => {
    if (!scan) return;
    setSaving(true);
    try {
      scan.notes = notes;
      const cached = JSON.parse(localStorage.getItem("blnScans") || "[]") as ScanResult[];
      const idx = cached.findIndex(s => s.id === scan.id);
      if (idx >= 0) { cached[idx].notes = notes; localStorage.setItem("blnScans", JSON.stringify(cached)); }
      await supabase.from("scans").update({ notes }).eq("id", scan.id);
      setSaved(true);
      setTimeout(() => setSaved(false), 2500);
    } catch (_) {}
    finally { setSaving(false); }
  };

  const downloadReport = () => {
    if (!scan) return;
    const r = scan.result;
    const content = [
      "BIOLUNGNET — AI ANALYSIS REPORT",
      "=".repeat(42),
      `File:           ${scan.fileName}`,
      `Date:           ${new Date(scan.date).toLocaleString()}`,
      `Scan ID:        ${scan.id}`,
      "",
      "RESULTS",
      "─".repeat(20),
      `Classification: ${r.classification}`,
      `Confidence:     ${r.confidence.toFixed(2)}%`,
      `Risk Level:     ${r.riskLevel}`,
      "",
      "PROBABILITY BREAKDOWN",
      "─".repeat(20),
      `Normal:         ${r.probabilities.normal.toFixed(2)}%`,
      `Benign:         ${r.probabilities.benign.toFixed(2)}%`,
      `Malignant:      ${r.probabilities.malignant.toFixed(2)}%`,
      "",
      "CLINICAL NOTES",
      "─".repeat(20),
      scan.notes || "No notes added.",
      "",
      "DISCLAIMER",
      "─".repeat(20),
      "This AI analysis is for assistance only.",
      "Always consult qualified healthcare professionals.",
      "",
      "Generated by BioLungNet AI Platform",
    ].join("\n");

    const a = document.createElement("a");
    a.href = URL.createObjectURL(new Blob([content], { type: "text/plain" }));
    a.download = `biolungnet-report-${scan.id}.txt`;
    a.click();
  };

  if (!scan) {
    return (
      <div style={{ minHeight: "100vh", background: "var(--dark)", display: "flex", alignItems: "center", justifyContent: "center" }}>
        <span className="bln-spinner" style={{ width: 36, height: 36, borderWidth: 3 }} />
      </div>
    );
  }

  const cls = scan.result.classification.toLowerCase() as "normal" | "benign" | "malignant";
  const clsColors: Record<string, string> = { normal: "var(--neon)", benign: "var(--warning)", malignant: "var(--danger)" };
  const clsColor = clsColors[cls] || "var(--neon)";
  const riskBadge: Record<string, string> = { Low: "bln-badge-s", Moderate: "bln-badge-w", High: "bln-badge-d" };
  const clsBadgeCls: Record<string, string> = { normal: "bln-badge-s", benign: "bln-badge-w", malignant: "bln-badge-d" };

  return (
    <div className="bln-page-in" style={{ minHeight: "100vh", background: "var(--dark)" }}>
      <nav className="bln-nav">
        <NavLogo />
        <div className="bln-nav-actions">
          <button className="bln-btn bln-btn-g bln-btn-sm" onClick={() => navigate("/dashboard")}>DASHBOARD</button>
        </div>
      </nav>

      <div style={{ maxWidth: 1060, margin: "0 auto", padding: "40px 24px" }}>
        <div style={{ display: "grid", gridTemplateColumns: "1fr 1fr", gap: 24 }} className="bln-two-col">
          {/* LEFT: Image + meta */}
          <div>
            <div className="bln-card" style={{ overflow: "hidden", animation: "bln-up .6s cubic-bezier(.22,1,.36,1) both" }}>
              <div style={{ padding: "14px 22px", borderBottom: "1px solid rgba(57,255,106,.1)", display: "flex", alignItems: "center", gap: 10, fontFamily: "var(--fd)", fontSize: 11, letterSpacing: 1, color: "var(--dim)" }}>
                <svg width="14" height="14" fill="none" viewBox="0 0 24 24" stroke="var(--neon)" strokeWidth="2"><rect x="3" y="3" width="18" height="18" rx="2"/><circle cx="8.5" cy="8.5" r="1.5"/><path d="M21 15l-5-5L5 21"/></svg>
                ORIGINAL SCAN
              </div>
              <div style={{ background: "#000", minHeight: 280, display: "flex", alignItems: "center", justifyContent: "center" }}>
                {scan.image
                  ? <img src={scan.image} alt="Scan" style={{ maxWidth: "100%", maxHeight: 350, objectFit: "contain" }} />
                  : <div style={{ color: "var(--dim)", fontSize: 14 }}>No preview available</div>
                }
              </div>
              <div style={{ padding: "12px 22px", background: "rgba(57,255,106,.02)", borderTop: "1px solid rgba(57,255,106,.07)" }}>
                {[["File", scan.fileName], ["Date", new Date(scan.date).toLocaleString()], ["ID", scan.id.substring(0, 18) + "…"]].map(([k, v]) => (
                  <div key={k} style={{ display: "flex", justifyContent: "space-between", fontSize: 12, color: "var(--dim)", padding: "2px 0" }}>
                    <span>{k}</span>
                    <strong style={{ color: "var(--muted)", maxWidth: 200, overflow: "hidden", textOverflow: "ellipsis", whiteSpace: "nowrap" }}>{v}</strong>
                  </div>
                ))}
              </div>
            </div>
          </div>

          {/* RIGHT: Results */}
          <div style={{ display: "flex", flexDirection: "column", gap: 16 }}>
            {/* Classification box */}
            <div className={`bln-result-box bln-rc-${cls[0]}`}>
              <div style={{ width: 56, height: 56, borderRadius: "50%", background: `${clsColor}1A`, boxShadow: `0 0 20px ${clsColor}40`, display: "flex", alignItems: "center", justifyContent: "center", flexShrink: 0 }}>
                <svg width="26" height="26" fill="none" viewBox="0 0 24 24" stroke={clsColor} strokeWidth="2">
                  {cls === "normal"    && <polyline points="20 6 9 17 4 12" />}
                  {cls === "benign"    && <><path d="M10.29 3.86L1.82 18a2 2 0 001.71 3h16.94a2 2 0 001.71-3L13.71 3.86a2 2 0 00-3.42 0z"/><line x1="12" y1="9" x2="12" y2="13"/><line x1="12" y1="17" x2="12.01" y2="17"/></>}
                  {cls === "malignant" && <><circle cx="12" cy="12" r="10"/><line x1="12" y1="8" x2="12" y2="12"/><line x1="12" y1="16" x2="12.01" y2="16"/></>}
                </svg>
              </div>
              <div>
                <div className={`bln-r${cls[0]}`}>{scan.result.classification.toUpperCase()}</div>
                <div style={{ fontSize: 11, color: "var(--dim)", marginTop: 2, letterSpacing: ".5px" }}>CLASSIFICATION RESULT</div>
              </div>
              <div style={{ marginLeft: "auto", textAlign: "right" }}>
                <span className={`bln-badge ${riskBadge[scan.result.riskLevel] || "bln-badge-s"}`}>{scan.result.riskLevel.toUpperCase()} RISK</span>
                <div style={{ fontFamily: "var(--fd)", fontSize: 26, fontWeight: 700, color: "var(--text)", marginTop: 6 }}>{scan.result.confidence.toFixed(1)}%</div>
                <div style={{ fontSize: 11, color: "var(--dim)", letterSpacing: ".5px" }}>CONFIDENCE</div>
              </div>
            </div>

            {/* Probability bars */}
            <div className="bln-card" style={{ padding: 22 }}>
              <p style={{ fontFamily: "var(--fd)", fontSize: 11, letterSpacing: 2, color: "var(--dim)", marginBottom: 18 }}>PROBABILITY BREAKDOWN</p>
              <div style={{ display: "flex", flexDirection: "column", gap: 16 }}>
                {([
                  ["NORMAL",    scan.result.probabilities.normal,    "var(--neon)",    ""],
                  ["BENIGN",    scan.result.probabilities.benign,    "var(--warning)", "warn"],
                  ["MALIGNANT", scan.result.probabilities.malignant, "var(--danger)",  "dang"],
                ] as [string, number, string, string][]).map(([label, val, color, mod], i) => (
                  <div key={label} className="bln-prob" style={{ animationDelay: `${i * 0.1}s` }}>
                    <div style={{ display: "flex", justifyContent: "space-between", fontFamily: "var(--fd)", fontSize: 11, letterSpacing: ".5px", color: "var(--dim)", marginBottom: 7 }}>
                      <span style={{ color }}>{label}</span>
                      <strong style={{ color: "var(--muted)" }}>{val.toFixed(1)}%</strong>
                    </div>
                    <div className="bln-progress-track">
                      <div className={`bln-progress-fill${mod ? " " + mod : ""} bln-pbar-fill`} style={{ width: 0, transition: "width .8s cubic-bezier(.4,0,.2,1)" }} data-w={val} />
                    </div>
                  </div>
                ))}
              </div>
            </div>

            {/* Notes */}
            <div className="bln-card" style={{ padding: 22 }}>
              <p style={{ fontFamily: "var(--fd)", fontSize: 11, letterSpacing: 2, color: "var(--dim)", marginBottom: 12 }}>CLINICAL NOTES</p>
              <textarea
                value={notes}
                onChange={e => setNotes(e.target.value)}
                placeholder="Add clinical observations..."
                style={{ width: "100%", minHeight: 100, padding: "13px 16px", border: "1px solid rgba(57,255,106,.2)", borderRadius: 8, fontFamily: "var(--fb)", fontSize: 14, color: "var(--text)", background: "rgba(57,255,106,.04)", outline: "none", resize: "vertical", transition: "border-color .25s, box-shadow .25s" }}
                onFocus={e => { e.target.style.borderColor = "var(--neon)"; e.target.style.boxShadow = "0 0 0 3px rgba(57,255,106,.12)"; }}
                onBlur={e => { e.target.style.borderColor = "rgba(57,255,106,.2)"; e.target.style.boxShadow = ""; }}
              />
              <div style={{ display: "flex", alignItems: "center", justifyContent: "space-between", marginTop: 12 }}>
                <span style={{ fontFamily: "var(--fd)", fontSize: 11, color: "var(--neon)", letterSpacing: 1 }}>
                  {saved ? "✓ SAVED" : ""}
                </span>
                <button className="bln-btn bln-btn-o bln-btn-sm" onClick={saveNotes} disabled={saving}>
                  {saving ? <><span className="bln-spinner" style={{ width: 12, height: 12, borderWidth: 1.5 }} /> SAVING</> : <>
                    <svg width="12" height="12" fill="none" viewBox="0 0 24 24" stroke="currentColor" strokeWidth="2"><path d="M19 21H5a2 2 0 01-2-2V5a2 2 0 012-2h11l5 5v11a2 2 0 01-2 2z"/><polyline points="17 21 17 13 7 13 7 21"/><polyline points="7 3 7 8 15 8"/></svg>
                    SAVE
                  </>}
                </button>
              </div>
            </div>

            {/* Actions */}
            <div style={{ display: "grid", gridTemplateColumns: "1fr 1fr", gap: 12 }}>
              <button className="bln-btn bln-btn-p" style={{ justifyContent: "center" }} onClick={downloadReport}>
                <svg width="14" height="14" fill="none" viewBox="0 0 24 24" stroke="currentColor" strokeWidth="2"><path d="M21 15v4a2 2 0 01-2 2H5a2 2 0 01-2-2v-4"/><polyline points="7 10 12 15 17 10"/><line x1="12" y1="15" x2="12" y2="3"/></svg>
                DOWNLOAD
              </button>
              <button className="bln-btn bln-btn-o" style={{ justifyContent: "center" }} onClick={() => navigate("/upload")}>
                <svg width="14" height="14" fill="none" viewBox="0 0 24 24" stroke="currentColor" strokeWidth="2"><polyline points="16 16 12 12 8 16"/><line x1="12" y1="12" x2="12" y2="21"/><path d="M20.39 18.39A5 5 0 0018 9h-1.26A8 8 0 103 16.3"/></svg>
                NEW SCAN
              </button>
            </div>

            <div className="bln-alert bln-alert-i" style={{ fontSize: 12 }}>
              <svg width="14" height="14" fill="none" viewBox="0 0 24 24" stroke="var(--accent)" strokeWidth="2"><circle cx="12" cy="12" r="10"/><line x1="12" y1="8" x2="12" y2="12"/><line x1="12" y1="16" x2="12.01" y2="16"/></svg>
              AI analysis is for assistance only. Always consult qualified healthcare professionals for medical decisions.
            </div>
          </div>
        </div>
      </div>
    </div>
  );
}
